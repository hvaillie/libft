int			ft_sqrtround(int nb)
{
	int		i;
	int		nbi;
	int		n;

	if (nb <= 0)
		return (0);
	i = 1;
	nbi = 1;
	n = 1;
	while (i < nb)
	{
		nbi++;
		n = n + 2;
		i += n;
	}
	return (nbi);
}
